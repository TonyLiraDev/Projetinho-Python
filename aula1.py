# valor por hora
# criação de um programa que retorna o valor hora de um funcionário com base no seu salário mensal e horas trabalhadas por mês

salario_mensal= input("Qual seu salário mensal?")
horas_trabalhadas= input("Quantas horas vôce trabalha por mês?")
valor_hora= int(salario_mensal)/int(horas_trabalhadas)
print(valor_hora)