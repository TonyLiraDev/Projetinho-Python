# encontre o maior entre dois nuúmeros

primeiro_valor= input("insira o pimeiro valor = ")
segundo_valor= input("insira o segundo valor = ")

if int(primeiro_valor) > int(segundo_valor):
  print("Primeiro valor é o maior")

elif int(primeiro_valor) < int(segundo_valor):
  print("Segundo valor é o maior")

else: print("Os números são iguais")